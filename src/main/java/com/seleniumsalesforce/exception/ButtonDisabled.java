package com.seleniumsalesforce.exception;

public class ButtonDisabled extends SdriverException{
	
    public ButtonDisabled() {
        super();
    }

    public ButtonDisabled(String message) {
        super(message);
    }

    public ButtonDisabled(String message, Throwable cause) {
        super(message, cause);
    }

    public ButtonDisabled(Throwable cause) {
        super(cause);
    }


}
